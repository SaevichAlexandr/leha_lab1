<p>Женат на Ивановой Екатерине Павловне, 05 мая 1991 года рождения. Родилась в городе Владивостоке, образование высшее, работает юристом. Проживает совместно со мной по адресу: город Владивосток, ул. Комсомольская, д. 15, кв. 5.</p>

