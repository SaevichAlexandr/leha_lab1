<h2>Основные правила написания автобиографии&nbsp;</h2>

<p>Следует сразу сказать, что никаких особых требований к написанию автобиографий не предусматривается. Следовательно, составлять автобиографию необходимо руководствуясь общими требованиями к написанию деловых писем.&nbsp;</p>

<ol>
	<li>Ваша автобиография не должна быть очень объемной. Постарайтесь быть лаконичными. Максимальный объем написанного не должен составлять более 1-2 листов текста. Как показывает практика, длинные &laquo;сочинения&raquo; не помогут вам раскрыться в глазах читающего &mdash; они скорее произведут обратный эффект.&nbsp;</li>
	<li>Написанное не должно содержать ошибок, общая форма изложения &mdash; деловой стиль. При рассмотрении вашей автобиографии, читающий будет уделять внимание не столько написанному, сколько тому, в какой форме это сделано. По этой причине грамотная речь позволит набрать вам &laquo;дополнительные баллы&raquo;.</li>
	<li>Все события, описанные вами, должны быть изложены в хронологическом порядке, логично и последовательно. То есть нельзя сразу после рассказа о школе перейти к трудовой деятельности, пропустив иные учебные заведения, или вначале рассказать о месте работы, а потом упомянуть о полученном образовании.</li>
	<li>Информация о вас, изложенная в автобиографии, должна быть подлинной. Включение ошибочных или недостоверных сведений может помешать получить желаемую работу (или добиться иной цели) и создать не самую хорошую деловую репутацию.&nbsp;</li>
</ol>

